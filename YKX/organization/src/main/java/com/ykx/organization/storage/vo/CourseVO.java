public class CourseVO implements Serializable {

    private int count;
    private String name;
    private String code;

    public CourseVO() {
    }

    public CourseVO(int count, String name, String code) {
        this.count = count;
        this.name = name;
        this.code = code;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
